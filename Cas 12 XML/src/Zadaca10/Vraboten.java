package Zadaca10;

public class Vraboten {
    public String ime;
    public String prezime;
    public String plata;
    
    public Vraboten(String ime, String prezime, String plata) {
    	this.ime=ime;
    	this.prezime=prezime;
    	this.plata=plata;
    }
    public String getIme() {
    	return ime;
    }
    public String getPrezime() {
    	return prezime;
    }
    public String getPlata() {
    	return plata;
    }
}
