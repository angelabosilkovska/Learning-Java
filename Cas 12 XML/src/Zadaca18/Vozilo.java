package Zadaca18;

public class Vozilo {
   private String marka;
   private String model;
   private String boja;
   private String cena;
   
   public Vozilo() {
	   marka=" ";
	   model=" ";
	   boja=" ";
	   cena=" ";
   }
   public String getMarka() {
	   return marka;
   }
   public String getModel() {
	   return model;
   }
   public String getBoja() {
	   return boja;
   }
   public String getCena() {
	   return cena;
   }
   public void setMarka() {
	   this.marka=marka;
   }
   public void setModel() {
	   this.model=model;
   }
   public void setBoja() {
	   this.boja=boja;
   }
   public void setCena() {
	   this.cena=cena;
   }
}
