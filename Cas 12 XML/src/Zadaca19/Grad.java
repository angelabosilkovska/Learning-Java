package Zadaca19;

public class Grad {
  private String ime;
  private String drzava; 
  private String brNaZiteli;

  public Grad() {
	  ime=" ";
	  drzava=" ";
	  brNaZiteli=" ";
  }
  public String getIme() {
	   return ime;
  }
  public String getDrzava() {
	   return drzava;
  }
  public String getBrNaZiteli() {
	   return brNaZiteli;
  }
  public void setIme() {
	   this.ime=ime;
  }
  public void setDrzava() {
	   this.drzava=drzava;
  }
  public void setBrNaZiteli() {
	   this.brNaZiteli=brNaZiteli;
  }
}
