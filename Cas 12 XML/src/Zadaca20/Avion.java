package Zadaca20;

public class Avion {
   private String marka;
   private String model; 
   private String cena;
   
   public Avion() {
	   marka=" ";
	   model=" ";
	   cena=" ";
   }
   public String getMarka() {
	   return marka;
   }
   public String getModel() {
	   return model;
   }
   public String getCena() {
	   return cena;
   }
   public void setMarka() {
	   this.marka=marka;
   }
   public void setModel() {
	   this.model=model;
   }
   public void setCena() {
	   this.cena=cena;
   }
}
