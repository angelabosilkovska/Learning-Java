package Zadaca17;

public class Doktor {
	   public String ime;
	   public String prezime;
	   public String specijalizacija;
	   public String plata;
	   
	   public Doktor() {
		   ime=" ";
		   prezime=" ";
		   specijalizacija=" ";
		   plata=" ";
	   }
	   public void setIme() {
		   this.ime=ime;
	   }
	   public void setPrezime() {
		   this.prezime=prezime;
	   }
	   public void setSpecijalizacija() {
		   this.specijalizacija=specijalizacija;
	   }
	   public void setPlata() {
		   this.plata=plata;
	   }
}
